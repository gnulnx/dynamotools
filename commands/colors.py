# highlight functions
def color_red(text):
    """
    Color text red.
    """
    return f"\033[1;31m{text}\033[0m"


def color_green(text):
    """
    Color text green.
    """
    return f"\033[1;32m{text}\033[0m"


def color_yellow(text):
    """
    Color text yellow.
    """
    return f"\033[1;33m{text}\033[0m" ""
